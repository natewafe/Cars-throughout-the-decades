/** Build per-car rows from cars.ts. The slug determines which sheet they
 *  go to. The map below is the only place where slug → display sheet name
 *  is configured. */
const SLUG_TO_SHEET: Record<string, string> = {
  countach: "Lamborghini",
  "959":    "Porsche",
  f1:       "McLaren",
  veyron:   "Bugatti",
};

function buildCarRows(): ContentRow[] {
  const carsFile = REL("src/lib/cars.ts");
  const rows: ContentRow[] = [];
  for (const car of cars) {
    const sheet = SLUG_TO_SHEET[car.slug];
    if (!sheet) continue;
    const page = `${car.maker} ${car.name}`;
    rows.push({ sheet, page, field: "Car name",   value: car.name,    file: carsFile });
    rows.push({ sheet, page, field: "Car maker",  value: car.maker,   file: carsFile });
    rows.push({ sheet, page, field: "Decade",     value: car.decade,  file: carsFile });
    rows.push({ sheet, page, field: "Tagline",    value: car.tagline, file: carsFile });
    rows.push({ sheet, page, field: "Hero caption", value: car.hero.caption, file: carsFile });
    car.essay.forEach((p, i) =>
      rows.push({ sheet, page, field: `Essay paragraph ${i + 1}`, value: p, file: carsFile })
    );
    car.stats.forEach((s, i) => {
      rows.push({ sheet, page, field: `Stat ${i + 1} label`, value: s.label, file: carsFile });
      rows.push({ sheet, page, field: `Stat ${i + 1} value`, value: s.value, file: carsFile });
    });
  }
  return rows;
}

/** Per-car scroll-scene captions + finale title (auto-generated file). */
function buildSceneRows(): ContentRow[] {
  const file = REL("src/lib/scenes.ts");
  const rows: ContentRow[] = [];
  for (const car of cars) {
    const sheet = SLUG_TO_SHEET[car.slug];
    if (!sheet) continue;
    const scene = scenesBySlug[car.slug];
    if (!scene) continue;
    const page = `${car.maker} ${car.name} — Scroll captions`;
    scene.captions.forEach((cap, i) => {
      rows.push({ sheet, page, field: `Scene caption ${i + 1} eyebrow`, value: cap.eyebrow, file });
      rows.push({ sheet, page, field: `Scene caption ${i + 1} line`,    value: cap.line,    file });
    });
    rows.push({ sheet, page, field: "Scene finale title", value: scene.finaleTitle, file });
  }
  return rows;
}

/** Per-car artifact figures (caption, blurb, alt) + footnotes (auto-gen). */
function buildArtifactRows(): ContentRow[] {
  const file = REL("src/lib/artifacts.ts");
  const rows: ContentRow[] = [];
  for (const car of cars) {
    const sheet = SLUG_TO_SHEET[car.slug];
    if (!sheet) continue;
    const data = artifactsBySlug[car.slug];
    if (!data) continue;

    // Primary source citation (per-car header text)
    const headerPage = `${car.maker} ${car.name} — Artifacts header`;
    rows.push({ sheet, page: headerPage, field: "Primary source citation", value: data.primarySource.citation, file });

    // Each figure
    let sectionTitle = "";
    let figIdx = 1;
    for (const item of data.artifacts) {
      if (item.kind === "section") {
        sectionTitle = item.title;
        const sectionPage = `${car.maker} ${car.name} — Section: ${sectionTitle}`;
        rows.push({ sheet, page: sectionPage, field: "Section title",      value: item.title,     file });
        rows.push({ sheet, page: sectionPage, field: "Section tier label", value: item.tierLabel, file });
        continue;
      }
      const fig = item;
      const page = `${car.maker} ${car.name} — Figure ${fig.figId} (${sectionTitle})`;
      rows.push({ sheet, page, field: `Fig ${fig.figId} blurb`,     value: fig.blurb,    file });
      rows.push({ sheet, page, field: `Fig ${fig.figId} caption`,   value: fig.caption,  file });
      rows.push({ sheet, page, field: `Fig ${fig.figId} alt text`,  value: fig.alt,      file });
      rows.push({ sheet, page, field: `Fig ${fig.figId} footnote`,  value: fig.footnote, file });
      rows.push({ sheet, page, field: `Fig ${fig.figId} filename`,  value: fig.filename, file });
      figIdx++;
    }

    // Footnotes (the consolidated array at the bottom of each car's data)
    data.footnotes.forEach((fn, i) => {
      rows.push({
        sheet,
        page: `${car.maker} ${car.name} — Footnotes`,
        field: `Footnote ${i + 1}`,
        value: fn,
        file,
      });
    });
  }
  return rows;
}

/** Bibliography entries — site-wide, alphabetized by author. */
function buildBibliographyRows(): ContentRow[] {
  const file = REL("src/lib/bibliography.ts");
  return bibliography.map((entry, i) => ({
    sheet: "Bibliography",
    page: "Bibliography",
    field: `Entry ${i + 1}`,
    value: entry,
    file,
  }));
}

/** Image references — these aren't text content, but listing the
 *  filename + its caption helps the user identify which file in
 *  /public/artifacts/<slug>/ corresponds to which figure when
 *  swapping in their own scans. */
function buildImageReferenceRows(): ContentRow[] {
  const rows: ContentRow[] = [];
  for (const car of cars) {
    const data = artifactsBySlug[car.slug];
    if (!data) continue;
    const dir = `public/artifacts/${data.imgDir}/`;
    let sectionTitle = "";
    for (const item of data.artifacts) {
      if (item.kind === "section") {
        sectionTitle = item.title;
        continue;
      }
      const fig = item;
      rows.push({
        sheet: "Images",
        page: `${car.maker} ${car.name}`,
        field: `Fig ${fig.figId} (${sectionTitle}) — file at ${dir}${fig.filename}`,
        value: fig.filename,
        file: REL("src/lib/artifacts.ts"),
      });
    }
  }
  // Per-car hero images from cars.ts
  for (const car of cars) {
    car.images.forEach((img, i) => {
      rows.push({
        sheet: "Images",
        page: `${car.maker} ${car.name} — Hero images`,
        field: `Hero image ${i + 1}`,
        value: img,
        file: REL("src/lib/cars.ts"),
      });
    });
  }
  return rows;
}

export function buildAllRows(): ContentRow[] {
  return [
    ...STATIC_ROWS,
    ...buildCarRows(),
    ...buildSceneRows(),
    ...buildArtifactRows(),
    ...buildBibliographyRows(),
    ...buildImageReferenceRows(),
  ];
}

