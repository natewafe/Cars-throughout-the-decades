/**
 * Single source of truth for editable site content.
 *
 * Each row describes ONE editable string:
 *   sheet  — Excel sheet it lives on
 *   page   — Column A (which page/section it belongs to)
 *   field  — Column B (human label, e.g. "Hero stat")
 *   value  — Column C (current text in the source file)
 *   file   — relative path of the source file to update
 *
 * The export script (export_content.ts) writes this to site_content.xlsx.
 * The import script (import_content.ts) reads the xlsx, finds rows with
 * a non-empty NEW VALUE column, and rewrites the matching `value` in the
 * `file` referenced here.
 *
 * NOTE on auto-generated files:
 *   - src/lib/scenes.ts and src/lib/artifacts.ts are AUTO-GENERATED by
 *     the Python build pipeline (_build_nextjs_data.py). Editing them
 *     directly works but the next regeneration will wipe your changes.
 *     We deliberately exclude scene captions / artifact captions from
 *     this export — edit them at the source (Markdown / Python).
 */

